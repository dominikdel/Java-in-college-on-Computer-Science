public class ZlozoneOperatory {
    public static void main(String[] args) {
        int a = 17;
        double b = 4.0;

        a += b;
        System.out.println("a += b  ->  a = " + a);

        a -= b;
        System.out.println("a -= b  ->  a = " + a);

        a *= b;
        System.out.println("a *= b  ->  a = " + a);

        a /= b;
        System.out.println("a /= b  ->  a = " + a);

        a %= b;
        System.out.println("a %= b  ->  a = " + a);
    }
}
